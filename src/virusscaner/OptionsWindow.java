    private void usernameBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_usernameBtnActionPerformed
        // TODO add your handling code here:
        Validation val = new Validation();
        if(User.userId.equals("999")){
            JOptionPane.showMessageDialog(null, "This cannot be used with skip at login");
        }else{
            String input = JOptionPane.showInputDialog("Please enter a new username!");
            if(input == null){
                ;
            }else if(val.pCheck(input)==false){
                JOptionPane.showMessageDialog(null, "Please enter username!");
            }else{
                changeUsername(input);
            }
        }
        
    }//GEN-LAST:event_usernameBtnActionPerformed

