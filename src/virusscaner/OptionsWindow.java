    /**
     *
     * @param newUser
     */
    public void changeUsername(String newUser){
        FileIO io = new FileIO();
        Search se = new Search();
        try {
            String[][] userLogin = io.read2dArray("UserLogin.txt", io.rowAmount("UserLogin.txt"), 3);
            int index = se.idSearch(User.userId, "UserLogin.txt");
            userLogin[index][1] = newUser;
            io.save2dArray("UserLogin.txt", userLogin, io.rowAmount("UserLogin.txt"));
            
        } catch (IOException ex) {
            Logger.getLogger(OptionsWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    /**
     *
     * @param newPass
     */
    public void changePassword(String newPass){
        FileIO io = new FileIO();
        Search se = new Search();
        try {
            String[][] userLogin = io.read2dArray("UserLogin.txt", io.rowAmount("UserLogin.txt"), 3);
            int index = se.idSearch(User.userId, "UserLogin.txt");
            userLogin[index][2] = newPass;
            io.save2dArray("UserLogin.txt", userLogin, io.rowAmount("UserLogin.txt"));
            
        } catch (IOException ex) {
            Logger.getLogger(OptionsWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
