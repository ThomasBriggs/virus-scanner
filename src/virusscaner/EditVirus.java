    public void addRecord() throws IOException{
        String id = EditRecord.Id;
        String dateandtime = new String(dateTxt.getText() + " " + timeTxt.getText());
        String[] userArray = {id ,dateandtime, dirTxt.getText(), String.valueOf(EditRecord.userAddedId)};

        FileIO io = new FileIO();
        Sort sort =new Sort();
        try {
            io.saveLine("log.txt", String.join(",", userArray), true);
            sort.bubbleSort2dArraySave("log.txt", 0, 4);
            JOptionPane.showMessageDialog(null, "The log has been edited!");
        } catch (IOException ex) {
            Logger.getLogger(EditVirus.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "There was an error!");
        }
        clear();
    }
    
