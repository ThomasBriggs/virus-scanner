    public boolean isFormValid(){
        Validation val = new Validation();
        String date = dateTxt.getText();
        String time = timeTxt.getText();
        String dir = dirTxt.getText();
        if(val.pCheck(date) == false){
            JOptionPane.showMessageDialog(null, "Please enter a date");
            return false;
        }
        if(!val.dateFormatCheckv2(date)){
            JOptionPane.showMessageDialog(null, "Please make sure date follows yyyy/mm/dd");
            return false;
        }
        if(!val.pCheck(time)){
            JOptionPane.showMessageDialog(null, "Please enter a time");
            return false;
        }
        if(!val.timeFormatCheck(time)){
            JOptionPane.showMessageDialog(null, "Please make sure time follows hh:mm:ss");
            return false;
        }
        if(!val.pCheck(dir)){
            JOptionPane.showMessageDialog(null, "Please enter a directory");
            return false;
        }
        if(dir.contains("/") == false && dir.contains("\\") == false){
            JOptionPane.showMessageDialog(null, "Please make sure directory contains a \\ or /");
            return false;
        }
        return true;
    }
    
