    /**
     *
     * @throws IOException
     */
    public void addRecord() throws IOException{
        String id = String.valueOf(genId("Users.txt"));
        String[] userArray = {id ,nameTxt.getText(), 
            dobTxt.getText(), deptTxt.getText(), accTxt.getText()};
        String[] userLoginArray = {id, usernameTxt.getText(), " "};

        FileIO io = new FileIO();
        try {
            io.saveLine("Users.txt", String.join(",", userArray), true);
            io.saveLine("UserLogin.txt", String.join(",", userLoginArray), true);
            JOptionPane.showMessageDialog(null, "The user has been added!");
        } catch (IOException ex) {
            Logger.getLogger(AddUser.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "There was an error!");
        }
        clear();
    }
    
