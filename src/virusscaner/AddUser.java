    public boolean isFormValid(){
        Validation val = new Validation();
        String name = nameTxt.getText();
        String userName = usernameTxt.getText();
        String dob = dobTxt.getText();
        String dep = deptTxt.getText();
        String acc = accTxt.getText();
        if(val.pCheck(name) == false){
            JOptionPane.showMessageDialog(null, "Please enter a name");
            return false;
        }
        if(!val.pCheck(userName)){
            JOptionPane.showMessageDialog(null, "Please enter a username");
            return false;
        }
        if(!val.pCheck(dob)){
            JOptionPane.showMessageDialog(null, "Please enter a date of birth");
            return false;
        }
        if(!val.dateFormatCheck(dob)){
            JOptionPane.showMessageDialog(null, "Please make sure dob follows dd/mm/yy");
            return false;
        }
        if(!val.pCheck(dep)){
            JOptionPane.showMessageDialog(null, "Please enter a department");
            return false;
        }
        if(!val.intCheck(dep)){
            JOptionPane.showMessageDialog(null, "Please enter a integer for department");
            return false;
        }
        if(!val.pCheck(acc)){
            JOptionPane.showMessageDialog(null, "Please enter a level of access");
            return false;
        }
        if(!val.intCheck(acc)){
            JOptionPane.showMessageDialog(null, "Please enter level of access as an integer");
            return false;
        }
        if(!val.rangeCheck(acc, 0, 5)){
            JOptionPane.showMessageDialog(null, "Please enter a level of access between 0 and 5");
            return false;
        }
        return true;
    }
    
    /**
     *
     * @param filename
     * @return
     * @throws IOException
     */
    public int genId(String filename) throws IOException{
        FileIO io = new FileIO();
        int[] usedId = new int[io.rowAmount(filename)];
        String[] line;
        int id;
        String[] ids = io.read(filename);
        for(int i = 0; i < ids.length; i++){
            line = ids[i].split(",");
            id = Integer.parseInt(line[0]);
            usedId[i] = id;
        }
        return usedId[usedId.length-1]+1;     
    }
    
